// JS é uma linguagem de tipagem dinâmica, ou seja, não é necessário declarar o tipo de dado que a variável irá receber

// String - dados do tipo texto
console.log("Mudkip"); // Para Strings, é necessário colocar eles dentro de "" ou ''
console.log('Mashtomp');
console.log(`Swampert`); // Pode-se utilizar também as ``

 // Para se utilizar qualquer uma das aspas dentro do texto, basta inverter a ordem da lógica delas
console.log("'Treecko'");
console.log('Grovyle');
console.log(`'Sceptile' "Level 36"`);


// Para números, não é necessário o uso de aspas
console.log(12345);
console.log(123.456); //

// É posível inserir vários valores dentro do log
console.log("Torchic", "LVL:", 10);
